function linkLeiaMais1() {
    var reticenciasLink = document.getElementById("reticenciasLink1");
    var textoOcultoLink = document.getElementById("textoOcultoLink1");
    var linkLeiaMais = document.getElementById("linkLeiaMais1");

    if (reticenciasLink.style.display == "none") {
        reticenciasLink.style.display = "inline";
        textoOcultoLink.style.display = "none";
        linkLeiaMais.innerHTML = "Leia mais";
    }
    else {
        reticenciasLink.style.display = "none";
        textoOcultoLink.style.display = "inline";
        linkLeiaMais.innerHTML = "Leia menos";
    }
}

